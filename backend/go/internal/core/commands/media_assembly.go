// Package commands provides the concrete implementations of the Chain of
// Responsibility (COR) pattern's Command interface. This file defines the
// command responsible for assembling the final, complete media object.
//
// Logic Flow:
// This command acts as a final assembly step in the media processing workflow.
// It takes the high-level summary information (title, cast, etc.) and the
// detailed, per-scene information generated by previous commands and combines
// them into a single, cohesive `model.Media` struct. This struct is then
// ready for persistence.
//
//  1. Receives two inputs from the context:
//     a) A `model.MediaSummary` struct.
//     b) A slice of strings, where each string is a JSON representation of a `model.Scene`.
//  2. Joins the scene JSON strings into a single valid JSON array string.
//  3. Unmarshals (parses) this JSON array into a slice of `model.Scene` structs.
//  4. Sorts the scenes chronologically based on their start time. This is
//     important because scene extraction may have happened in parallel and out of order.
//  5. Re-sequences the scenes by assigning a new sequence number after sorting.
//  6. Creates a new `model.Media` object (which also generates a UUID for the ID).
//  7. Populates the `model.Media` object with all the data from the summary
//     and the newly sorted and sequenced scenes.
//  8. Places the final, assembled `model.Media` object back into the context
//     for the next command (likely persistence) to use.
package commands

import (
	"encoding/json"
	"fmt"
	"sort"
	"strconv"
	"strings"
	"time"

	"github.com/jaycherian/gcp-go-media-search/internal/core/cor"
	"github.com/jaycherian/gcp-go-media-search/internal/core/model"
)

// DefaultMovieTimeFormat defines the expected time format (HH:MM:SS) for scene start/end times.
const (
	DefaultMovieTimeFormat = "15:04:05"
)

// MediaAssembly is a command that combines a media summary and a collection of scenes
// into a single, complete Media object.
type MediaAssembly struct {
	cor.BaseCommand
	summaryParam     string // The context key for the input MediaSummary struct.
	sceneParam       string // The context key for the input slice of scene JSON strings.
	mediaObjectParam string // The context key where the final assembled Media object will be stored.
	mediaLengthParam string
}

// NewMediaAssembly is the constructor for the MediaAssembly command.
//
// Inputs:
//   - name: A string name for this command instance.
//   - summaryParam: The name of the context parameter holding the `model.MediaSummary`.
//   - sceneParam: The name of the context parameter holding the slice of scene JSON strings.
//   - mediaObjectParam: The name of the context parameter to store the output `model.Media` object.
//
// Outputs:
//   - *MediaAssembly: A pointer to the newly instantiated command.
func NewMediaAssembly(name string, summaryParam string, sceneParam string, mediaObjectParam string, mediaLengthParam string) *MediaAssembly {
	return &MediaAssembly{
		BaseCommand:      *cor.NewBaseCommand(name),
		summaryParam:     summaryParam,
		sceneParam:       sceneParam,
		mediaObjectParam: mediaObjectParam,
		mediaLengthParam: mediaLengthParam,
	}
}

// IsExecutable overrides the default behavior to ensure that both the summary
// and scene parameters are present in the context before execution.
//
// Inputs:
//   - context: The shared `cor.Context` for this workflow execution.
//
// Outputs:
//   - bool: True if the required parameters exist, otherwise false.
func (m *MediaAssembly) IsExecutable(context cor.Context) bool {
	return context != nil &&
		context.Get(m.summaryParam) != nil &&
		context.Get(m.sceneParam) != nil
}

// Execute performs the assembly logic.
//
// Inputs:
//   - context: The shared `cor.Context` for this workflow execution.
func (m *MediaAssembly) Execute(context cor.Context) {
	// Retrieve the inputs from the context.
	summary := context.Get(m.summaryParam).(*model.MediaSummary)
	jsonScenes := context.Get(m.sceneParam).([]string)
	mediaLengthInSeconds := context.Get(m.mediaLengthParam).(int)

	// The scene data comes in as a slice of JSON strings: `["{...}", "{...}"]`.
	// We join them with a comma and wrap them in brackets to create a valid JSON array string: `"[ {...} , {...} ]"`.
	sceneValues := fmt.Sprintf("[ %s ]", strings.Join(jsonScenes, ","))

	// Unmarshal the JSON array string into a slice of Scene structs.
	scenes := make([]*model.Scene, 0)
	sceneErr := json.Unmarshal([]byte(sceneValues), &scenes)
	if sceneErr != nil {
		m.GetErrorCounter().Add(context.GetContext(), 1)
		context.AddError(m.GetName(), sceneErr)
		return
	}

	if len(scenes) == 0 { // If no scenes were extracted, create a default scene with the summary.
		defaultScene := &model.Scene{
			SequenceNumber: 0,
			Start:          "00:00:00",
			End:            formatSeconds(mediaLengthInSeconds),
			Script:         summary.Summary,
		}
		scenes = append(scenes, defaultScene)
	}

	// Correct timestamps if they are out of bounds due to LLM mix-ups
	for _, scene := range scenes {
		scene.Start = correctTimestamp(scene.Start, mediaLengthInSeconds)
		scene.End = correctTimestamp(scene.End, mediaLengthInSeconds)
	}

	// Sort the scenes chronologically. This is crucial because scene extraction
	// may have happened in parallel, so the results are not guaranteed to be in order.
	sort.Slice(scenes, func(i, j int) bool {
		// Parse the start time strings into time objects for comparison.
		t, _ := time.Parse(DefaultMovieTimeFormat, scenes[i].Start)
		tt, _ := time.Parse(DefaultMovieTimeFormat, scenes[j].Start)
		return t.Before(tt)
	})

	// After sorting, re-apply the sequence numbers to ensure they are correct.
	for i, scene := range scenes {
		scene.SequenceNumber = i + 1 // Use 1-based indexing for sequences.
	}

	// Create a new Media object. The constructor handles generating a unique ID.
	media := model.NewMedia(summary.Title)

	// Populate the Media object with data from the summary and the processed scenes.
	media.Title = summary.Title
	media.Category = summary.Category
	media.Summary = summary.Summary
	media.MediaUrl = summary.MediaUrl
	media.LengthInSeconds = summary.LengthInSeconds
	media.Director = summary.Director
	media.ReleaseYear = summary.ReleaseYear
	media.Genre = summary.Genre
	media.Rating = summary.Rating
	media.Cast = append(media.Cast, summary.Cast...)
	media.Scenes = append(media.Scenes, scenes...)

	m.GetSuccessCounter().Add(context.GetContext(), 1)

	// Place the fully assembled Media object into the specified output parameter in the context.
	context.Add(m.mediaObjectParam, media)
	// Also place it in the default output parameter for chain compatibility.
	context.Add(cor.CtxOut, media)
}

func formatSeconds(totalSeconds int) string {
	hours := totalSeconds / 3600
	minutes := (totalSeconds % 3600) / 60
	seconds := totalSeconds % 60
	return fmt.Sprintf("%02d:%02d:%02d", hours, minutes, seconds)
}

// correctTimestamp attempts to fix malformed HH:MM:SS timestamps that are out of
// the video's duration range. It checks for a common LLM error where minutes
// are written as hours and seconds as minutes.
func correctTimestamp(timestampStr string, videoLength int) string {
	parts := strings.Split(timestampStr, ":")
	if len(parts) != 3 {
		return timestampStr
	}

	h, errH := strconv.Atoi(parts[0])
	m, errM := strconv.Atoi(parts[1])
	s, errS := strconv.Atoi(parts[2])

	if errH != nil || errM != nil || errS != nil {
		return timestampStr
	}

	originalSeconds := h*3600 + m*60 + s

	// If the timestamp is already valid, return it.
	if originalSeconds <= videoLength {
		return timestampStr
	}

	// The timestamp is out of bounds. Let's check for a common mix-up:
	// HH:MM:SS from the LLM should have been 00:HH:MM.
	correctedSeconds := h*60 + m
	if correctedSeconds <= videoLength {
		correctedTimestamp := fmt.Sprintf("00:%02d:%02d", h, m)
		return correctedTimestamp
	}

	// If correction is still out of bounds, clamp to video length as a last resort.
	clampedTimestamp := formatSeconds(videoLength)
	return clampedTimestamp
}
